public class Ejemplo {
    public static void main(String args[]) {
        int valorEntero = 10/3; // 32 bits
        float valorFloat = 10f/3f; // 32 bits
        double valorDouble = 10d/3d; // 64 bits

        System.out.println("Valor entero: " + valorEntero);
        System.out.println("Valor flotante: " + valorFloat);
        System.out.println("Valor entero: " + valorDouble);
    }
}
