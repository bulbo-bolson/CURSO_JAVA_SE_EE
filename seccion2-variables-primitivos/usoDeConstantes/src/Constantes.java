public class Constantes {
    public static void main (String args[]) {
        // se utiliza el modificador final para definir un valor inmutable
        final int DIAS_SEMANA = 7;
        final int DIAS_LABORALES = 5;

        System.out.println("El número de días de la semana es: " + DIAS_SEMANA);
        System.out.println("El número de días laborables de la semana es: " + DIAS_LABORALES);

    }
}
